CREATE TABLE dates(
    ID INT NOT NULL,
    ORIGIN VARCHAR(255) NOT NULL,
    LABNR VARCHAR(255),
    C14AGE INT NOT NULL,
    C14STD INT NOT NULL,
    C13 FLOAT,
    MATERIAL VARCHAR(255),
    SPECIES VARCHAR(255),
    COUNTRY VARCHAR(255),
    FEATURE VARCHAR(1000),
    FEATURE_DESC VARCHAR(1000),
    SITE VARCHAR(255),
    PERIOD VARCHAR(255),
    CULTURE VARCHAR(255),
    Phase VARCHAR(255),
    LOCUS VARCHAR(1000),
    LATITUDE FLOAT,
    LONGITUDE FLOAT,
    METHOD VARCHAR(255),
    CALAGE INT,
    CALSTD INT,
    REFERENCE VARCHAR(1000),
    NOTICE VARCHAR(1000),
    CONSTRAINT DATE_ID PRIMARY KEY (ID, ORIGIN)
);


